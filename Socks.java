// Jhon is getting late for college. She is looking for a matching pair of socks from a box full of socks. 
// In the worst-case scenario, how many socks should Jhon remove from her drawer until she finds a matching pair.

import java.util.*;
public class Socks {
    public static void main(String[] args) {
        try (Scanner sc = new Scanner(System.in)) {
            int t= sc.nextInt();
            while(t-->0){
                int n= sc.nextInt();
                System.out.println(n+1);
            }
        }
    }
}
